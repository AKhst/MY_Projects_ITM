# Задача 3 - Создаем список list_3 с элементами ["a", "s", "1", "a", "32", "23"]. И
# спользуем list comprehension (генератор списка) для разделения элементов списка на два
# новых списка: letters и numbers. Методы isalpha() и isdigit() используются для проверки,
# является ли элемент буквой или числом соответственно.

list_3 = ["a", "s", "1", "a", "32", "23"]
letters = [char for char in list_3 if char.isalpha()]
numbers = [numm for numm in list_3 if numm.isdigit()]
print("Список с буквами:", letters)
print("Список с числами:", numbers)

