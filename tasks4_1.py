# Задача 2 - Создаем список list_2 с элементами ["Ростов", "+", "на", "-", "Дону"].
# Находим индекс элемента со значением + в списке с помощью метода index() и заменяем
# его на - присваиванием нового значения по найденному индексу. Затем объединяем
# элементы списка в одну строку с помощью метода join().

list_2 = ["Ростов", "+", "на", "-", "Дону"]
list_2[list_2.index("+")] = "-"
print("Название города:", '-'.join(list_2))
